function daysLeftAr(n) {
  if (n === 1) return 'تبقّى يوم واحد'
  if (n === 2) return 'تبقّى يومان'
  if (n >= 3 && n <= 10) return `تبقّت ${n} أيام`
  return `تبقّى ${n} يومًا`
}

function gbLeftAr(n) {
  return `تبقّى ${n} جيجا`
}

function daysLeftEn(n) {
  return `${n} day${n === 1 ? '' : 's'} left`
}

function gbLeftEn(n) {
  return `${n} GB left`
}

